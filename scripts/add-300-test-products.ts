function getRandomStatus(): 'REGULAR' | 'HIT' | 'NEW' | 'CLASSIC' | 'BANNER' {
  const statuses = ['REGULAR', 'HIT', 'NEW', 'CLASSIC', 'BANNER'] as const
  const weights = [0.5, 0.15, 0.15, 0.15, 0.05] // 50% regular, 15% hit, 15% new, 15% classic, 5% banner
  
  const random = Math.random()
  let cumulative = 0
  
  for (let i = 0; i < statuses.length; i++) {
    cumulative += weights[i]
    if (random <= cumulative) {
      return statuses[i]
    }
  }
  
  return 'REGULAR'
}

